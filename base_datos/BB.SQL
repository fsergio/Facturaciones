DROP VIEW V_VENTA;




CREATE VIEW V_VENTA (ID_VENTA, FECHA, ID_CLIENTE, NOMBRE_CLIENTE, VTOTAL)
AS SELECT V.ID_VENTA,V.FECHA,V.ID_CLIENTE,C.NOMBRE_CLIENTE,SUM(DV.SUBTOTAL_VENTA) AS VTOTAL
FROM VENTAS V INNER JOIN DETALLE_VENTAS DV ON V.ID_VENTA = DV.ID_VENTA
 INNER JOIN CLIENTES C ON V.ID_CLIENTE = C.ID_CLIENTE
GROUP BY V.ID_VENTA,V.FECHA,V.ID_CLIENTE,C.NOMBRE_CLIENTE;



